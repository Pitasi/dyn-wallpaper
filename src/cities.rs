use std::collections::HashMap;

// Macro for create something similar to a HashMap literal,
// while also escaping string keys
macro_rules! map(
    { $($key:expr => $lat:expr, $lon:expr, $elev:expr);+ } => {
        {
            let mut m = HashMap::new();
            $(
                let name = String::from($key);
                m.insert(
                    escape(&name),
                    City {
                        name: name,
                        latitude: $lat,
                        longitude: $lon,
                        elevation: $elev
                    }
                );
            )+
            m
        }
     };
);

#[derive(Debug)]
pub struct City {
    pub name: String,
    pub latitude: f64,
    pub longitude: f64,
    pub elevation: f64,
}

pub fn escape(string: &String) -> String {
    (*string).trim().replace(" ", "").to_lowercase()
}

